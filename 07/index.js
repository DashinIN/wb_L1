// 7.	Задача о коллекции функций: у вас есть массив функций, напишите код, который вызовет каждую функцию в
//  этом массиве и выведет их порядковый номер. Однако, вызов каждой функции должен происходить только после 
//  вызова предыдущей функции.

// Для этого создадим функцию callFunctions, которая будет вызывать функции из массива functions по одной,
//  передавая им порядковый номер в качестве аргумента. После вызова каждой функции, функция callFunctions
//   будет вызывать саму себя с увеличенным порядковым номером, до тех пор, пока все функции не будут вызваны.


function callFunctions(functions, index) {
    // Проверяем, что порядковый номер не превысил размер массива функций
    if (index < functions.length) {
      // Вызываем функцию из массива с текущим порядковым номером
      functions[index](index + 1);
      
      // Вызываем саму себя для следующей функции с увеличенным порядковым номером
      callFunctions(functions, index + 1);
    }
  }
  
  // Пример функций
  function func1(index) {
    console.log(`Вызвана функция номер ${index}`);
  }
  
  function func2(index) {
    console.log(`Вызвана функция номер ${index}`);
  }
  
  function func3(index) {
    console.log(`Вызвана функция номер ${index}`);
  }
  
  // Массив функций
  const functions = [func1, func2, func3];
  
  // Вызываем функцию начиная с первой функции и порядковым номером 1
  callFunctions(functions, 0);